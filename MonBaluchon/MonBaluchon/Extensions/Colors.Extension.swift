        return CGColor(red: 48.0, green: 56.0, blue: 65.0, alpha: 1.0)
    }
    
    class var appLightBG: CGColor {
        return CGColor(red: 58.0, green: 71.0, blue: 80.0, alpha: 1.0)
    }
    
    class var appElements: CGColor {
        return CGColor(red: 0.0, green: 173.0, blue: 181.0, alpha: 1.0)
    }
    
    class var appText: CGColor {
        return CGColor(red: 238.0, green: 238.0, blue: 238.0, alpha: 1.0)
    }
}
